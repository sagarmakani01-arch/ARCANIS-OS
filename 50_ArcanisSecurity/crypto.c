/* Round constants */
static const uint8_t rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36
};

/* Helper functions */
static uint8_t xtime(uint8_t x) {
    return (x << 1) ^ ((x & 0x80) ? 0x1B : 0x00);
}

static uint8_t mul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        uint8_t hi = a & 0x80;
        a <<= 1;
        if (hi) a ^= 0x1B;
        b >>= 1;
    }
    return p;
}

void aes_xor_block(uint8_t* dst, const uint8_t* a, const uint8_t* b) {
    for (int i = 0; i < AES_BLOCK_SIZE; i++)
        dst[i] = a[i] ^ b[i];
}

/* Key expansion */
static void key_expand(aes_ctx_t* ctx, const uint8_t* key, aes_key_size_t key_size) {
    int nk = key_size / 4;
    int nr = nk + 6;
    ctx->nr = nr;

    /* Copy key */
    for (int i = 0; i < nk * 4; i++)
        ctx->round_key[i] = key[i];

    /* Expand */
    for (int i = nk; i < (nr + 1) * 4; i++) {
        uint8_t temp[4];
        for (int j = 0; j < 4; j++)
            temp[j] = ctx->round_key[(i - 1) * 4 + j];

        if (i % nk == 0) {
            /* RotWord + SubWord + Rcon */
            uint8_t t = temp[0];
            temp[0] = sbox[temp[1]] ^ rcon[i / nk];
            temp[1] = sbox[temp[2]];
            temp[2] = sbox[temp[3]];
            temp[3] = sbox[t];
        } else if (nk > 6 && i % nk == 4) {
            temp[0] = sbox[temp[0]];
            temp[1] = sbox[temp[1]];
            temp[2] = sbox[temp[2]];
            temp[3] = sbox[temp[3]];
        }

        for (int j = 0; j < 4; j++)
            ctx->round_key[i * 4 + j] = ctx->round_key[(i - nk) * 4 + j] ^ temp[j];
    }
}

int aes_init(aes_ctx_t* ctx, const uint8_t* key, aes_key_size_t key_size) {
    if (!ctx || !key) return -1;
    if (key_size != AES_128 && key_size != AES_192 && key_size != AES_256) return -1;

    memset(ctx, 0, sizeof(aes_ctx_t));
    key_expand(ctx, key, key_size);
    return 0;
}

void aes_set_iv(aes_ctx_t* ctx, const uint8_t* iv) {
    if (ctx && iv) memcpy(ctx->iv, iv, AES_BLOCK_SIZE);
}

/* ---- ECB mode ---- */

static void add_round_key(uint8_t* state, const uint8_t* key) {
    for (int i = 0; i < 16; i++) state[i] ^= key[i];
}

static void sub_bytes(uint8_t* state) {
    for (int i = 0; i < 16; i++) state[i] = sbox[state[i]];
}

static void inv_sub_bytes(uint8_t* state) {
    for (int i = 0; i < 16; i++) state[i] = inv_sbox[state[i]];
}

static void shift_rows(uint8_t* state) {
    uint8_t tmp;
    /* Row 1: shift left 1 */
    tmp = state[1]; state[1] = state[5]; state[5] = state[9]; state[9] = state[13]; state[13] = tmp;
    /* Row 2: shift left 2 */
    tmp = state[2]; state[2] = state[10]; state[10] = tmp;
    tmp = state[6]; state[6] = state[14]; state[14] = tmp;
    /* Row 3: shift left 3 */
    tmp = state[15]; state[15] = state[11]; state[11] = state[7]; state[7] = state[3]; state[3] = tmp;
}

static void inv_shift_rows(uint8_t* state) {
    uint8_t tmp;
    /* Row 1: shift right 1 */
    tmp = state[13]; state[13] = state[9]; state[9] = state[5]; state[5] = state[1]; state[1] = tmp;
    /* Row 2: shift right 2 */
    tmp = state[2]; state[2] = state[10]; state[10] = tmp;
    tmp = state[6]; state[6] = state[14]; state[14] = tmp;
    /* Row 3: shift right 3 */
    tmp = state[3]; state[3] = state[7]; state[7] = state[11]; state[11] = state[15]; state[15] = tmp;
}

static void mix_columns(uint8_t* state) {
    for (int i = 0; i < 16; i += 4) {
        uint8_t a[4], b[4];
        for (int j = 0; j < 4; j++) {
            a[j] = state[i + j];
            b[j] = xtime(state[i + j]);
        }
        state[i + 0] = b[0] ^ a[1] ^ b[1] ^ a[2] ^ a[3];
        state[i + 1] = a[0] ^ b[1] ^ a[2] ^ b[2] ^ a[3];
        state[i + 2] = a[0] ^ a[1] ^ b[2] ^ a[3] ^ b[3];
        state[i + 3] = a[0] ^ a[1] ^ a[2] ^ b[3] ^ b[0];
    }
}

static void inv_mix_columns(uint8_t* state) {
    for (int i = 0; i < 16; i += 4) {
        uint8_t a[4];
        for (int j = 0; j < 4; j++) a[j] = state[i + j];
        state[i + 0] = mul(a[0], 0x0E) ^ mul(a[1], 0x0B) ^ mul(a[2], 0x0D) ^ mul(a[3], 0x09);
        state[i + 1] = mul(a[0], 0x09) ^ mul(a[1], 0x0E) ^ mul(a[2], 0x0B) ^ mul(a[3], 0x0D);
        state[i + 2] = mul(a[0], 0x0D) ^ mul(a[1], 0x09) ^ mul(a[2], 0x0E) ^ mul(a[3], 0x0B);
        state[i + 3] = mul(a[0], 0x0B) ^ mul(a[1], 0x0D) ^ mul(a[2], 0x09) ^ mul(a[3], 0x0E);
    }
}

int aes_encrypt_ecb(aes_ctx_t* ctx, const uint8_t plaintext[AES_BLOCK_SIZE],
                     uint8_t ciphertext[AES_BLOCK_SIZE]) {
    if (!ctx || !plaintext || !ciphertext) return -1;

    uint8_t state[16];
    memcpy(state, plaintext, 16);

    add_round_key(state, ctx->round_key);

    for (int round = 1; round < ctx->nr; round++) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, ctx->round_key + round * 16);
    }

    sub_bytes(state);
    shift_rows(state);
    add_round_key(state, ctx->round_key + ctx->nr * 16);

    memcpy(ciphertext, state, 16);
    return 0;
}

int aes_decrypt_ecb(aes_ctx_t* ctx, const uint8_t ciphertext[AES_BLOCK_SIZE],
                     uint8_t plaintext[AES_BLOCK_SIZE]) {
    if (!ctx || !ciphertext || !plaintext) return -1;

    uint8_t state[16];
    memcpy(state, ciphertext, 16);

    add_round_key(state, ctx->round_key + ctx->nr * 16);

    for (int round = ctx->nr - 1; round > 0; round--) {
        inv_shift_rows(state);
        inv_sub_bytes(state);
        add_round_key(state, ctx->round_key + round * 16);
        inv_mix_columns(state);
    }

    inv_shift_rows(state);
    inv_sub_bytes(state);
    add_round_key(state, ctx->round_key);

    memcpy(plaintext, state, 16);
    return 0;
}

/* ---- CBC mode ---- */

int aes_encrypt_cbc(aes_ctx_t* ctx, const uint8_t* plaintext, uint32_t len,
                     uint8_t* ciphertext) {
    if (!ctx || !plaintext || !ciphertext) return -1;

    uint8_t prev[AES_BLOCK_SIZE];
    memcpy(prev, ctx->iv, AES_BLOCK_SIZE);

    for (uint32_t i = 0; i < len; i += AES_BLOCK_SIZE) {
        uint8_t block[AES_BLOCK_SIZE];
        for (int j = 0; j < AES_BLOCK_SIZE; j++)
            block[j] = plaintext[i + j] ^ prev[j];

        aes_encrypt_ecb(ctx, block, ciphertext + i);
        memcpy(prev, ciphertext + i, AES_BLOCK_SIZE);
    }
    return 0;
}

int aes_decrypt_cbc(aes_ctx_t* ctx, const uint8_t* ciphertext, uint32_t len,
                     uint8_t* plaintext) {
    if (!ctx || !ciphertext || !plaintext) return -1;

    uint8_t prev[AES_BLOCK_SIZE];
    memcpy(prev, ctx->iv, AES_BLOCK_SIZE);

    for (uint32_t i = 0; i < len; i += AES_BLOCK_SIZE) {
        uint8_t block[AES_BLOCK_SIZE];
        aes_decrypt_ecb(ctx, ciphertext + i, block);

        for (int j = 0; j < AES_BLOCK_SIZE; j++)
            plaintext[i + j] = block[j] ^ prev[j];

        memcpy(prev, ciphertext + i, AES_BLOCK_SIZE);
    }
    return 0;
}

/* ---- PKCS7 padding ---- */

void aes_pkcs7_pad(const uint8_t* data, uint32_t len, uint8_t* padded, uint32_t* padded_len) {
    uint32_t pad_len = AES_BLOCK_SIZE - (len % AES_BLOCK_SIZE);
    *padded_len = len + pad_len;
    memcpy(padded, data, len);
    for (uint32_t i = len; i < *padded_len; i++)
        padded[i] = (uint8_t)pad_len;
}

int aes_pkcs7_unpad(const uint8_t* data, uint32_t len, uint8_t* unpadded, uint32_t* unpadded_len) {
    if (len == 0 || len % AES_BLOCK_SIZE != 0) return -1;
    uint8_t pad = data[len - 1];
    if (pad == 0 || pad > AES_BLOCK_SIZE) return -1;
    *unpadded_len = len - pad;
    memcpy(unpadded, data, *unpadded_len);
    return 0;
}
